package com.leetcode.learn.list;

public class ListNode {
    public int val;
    public ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

    public static ListNode getListNode() {
        return new ListNode(1, new ListNode(2, new ListNode(5, null)));
    }
}